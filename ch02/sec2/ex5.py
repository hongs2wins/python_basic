# 연산자 : 산술연산, 관계연산, 논리연산, 대입연산, 비트연산, 기타연산
num1 = 120
num2 = 27
print( "num1+num2=",(num1+num2)) # 120+27=147
print("num1-num2=",(num1-num2)) # 120-27=93
print("num1*num2=",(num1*num2)) # 120*27=3240
print("num1/num2=",(num1/num2)) # 120/27=4.44444...
print("num1%num2=",(num1%num2)) # 120을 27로 나눈 나머지 = 12
print("num1//num2=",(num1//num2)) # 120을 27로 나눈 몫 = 4
print("num1**2=",(num1**num2)) # 120의 2승(제곱) = 13737055...

