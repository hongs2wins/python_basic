# 비교(관계) 연산자 ; ==, !=, >, <, >=, <=
num1 = 120
num2 = 217
print("num1==num2 : ",(num1==num2))
print("num1!=num2 : ",(num1!=num2))
print("num1>num2 : ",(num1>num2))
print("num1>num2-100 : ",(num1>num2-100))
print("num1<num2 : ",(num1<num2))
print("num1+100<num2 : ",(num1+100<num2))
print("num1>=num2 : ",(num1>=num2))
print("num1<=num2 : ",(num1<=num2))
